     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $pay = new Payment();
        $pay->client_id = $request->client_id;
        $pay->payment = $request->pay_ammount;
        $done = $pay->save();
        if ($done) {
            $clientBottle = new ClientBottle();
            $clientBottle->client_id = $request->client_id;
            $clientBottle->pay_bottle = $request->pay_bottle;
            $clientBottle->save();
        }
    return redirect('/clients')->with('insertsuccess',"Pay Successfull");
    }

